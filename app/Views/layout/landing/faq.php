                <details open>
                    <summary><span class="faq-item-icon faq-icon-help" aria-hidden="true"></span><span>Apakah hasil SiPASTI menggantikan dokter?</span></summary>
                    <p>Tidak. Hasil sistem adalah konsultasi awal dan tetap perlu ditindaklanjuti dengan tenaga kesehatan.</p>
                </details>
                <details>
                    <summary><span class="faq-item-icon faq-icon-note" aria-hidden="true"></span><span>Data apa yang perlu diisi?</span></summary>
                    <p>Pengguna mengisi data anak dan memilih gejala yang tersedia pada halaman konsultasi.</p>
                </details>
                <details>
                    <summary><span class="faq-item-icon faq-icon-form" aria-hidden="true"></span><span>Bagaimana memulai konseling?</span></summary>
                    <p>Klik tombol Mulai Konseling, lalu ikuti formulir konsultasi sampai hasil analisis muncul.</p>
                </details>
                <details>
                    <summary><span class="faq-item-icon faq-icon-chart" aria-hidden="true"></span><span>Apa arti risiko rendah, sedang, dan tinggi?</span></summary>
                    <p>Risiko rendah berarti tetap dipantau, risiko sedang perlu perhatian lebih, dan risiko tinggi sebaiknya segera dikonsultasikan ke tenaga kesehatan.</p>
                </details>
                <details>
                    <summary><span class="faq-item-icon faq-icon-shield" aria-hidden="true"></span><span>Apakah data anak akan tersimpan?</span></summary>
                    <p>Data konsultasi tersimpan untuk membantu admin melihat riwayat pemeriksaan dan membuat laporan bila diperlukan.</p>
                </details>
                <details>
                    <summary><span class="faq-item-icon faq-icon-pin" aria-hidden="true"></span><span>Kapan harus datang ke puskesmas?</span></summary>
                    <p>Datang ke puskesmas jika hasil menunjukkan risiko sedang atau tinggi, atau jika orang tua merasa pertumbuhan anak perlu diperiksa langsung.</p>
                </details>
